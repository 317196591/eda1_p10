print("Ingresa un numero del 1 al 5")
numero=int(input())
if numero==1:
    print("El numero es 1")
elif numero==2:
    print("El numero es 2")
elif numero==3:
    print("El numero es 3")
elif numero==4:
    print("El numero es 4")
elif numero==5:
    print("El numero es 5")
else:
    print("Error")
